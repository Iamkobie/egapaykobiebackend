// ─── Seed Logic ──────────────────────────────────────────────────────────────

/**
 * Prepare a program record for insertion.
 * Adds the embedding_text field (title + summary) and sets scraped_at.
 */
function prepareRecord(program) {
  return {
    ...program,
    embedding_text: program.embedding_text || `${program.title} ${program.summary}`,
    source_url: program.official_url,
    scraped_at: new Date().toISOString(),
    scraper_metadata: { source: 'seed-script', version: '1.0.0' },
  };
}

/**
 * Insert records in batches to respect Supabase limits.
 */
async function insertInBatches(records, batchSize = 20) {
  const total = records.length;
  let inserted = 0;

  for (let i = 0; i < total; i += batchSize) {
    const batch = records.slice(i, i + batchSize);
    const { data, error } = await supabase
      .from('services')
      .insert(batch)
      .select('id');

    if (error) {
      console.error(`❌ Error inserting batch ${Math.floor(i / batchSize) + 1}:`, error.message);
      throw error;
    }

    inserted += data.length;
    console.log(`  ✓ Batch ${Math.floor(i / batchSize) + 1}: inserted ${data.length} records (${inserted}/${total})`);
  }

  return inserted;
}

